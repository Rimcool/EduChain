    }

    /**
     * Update the specified degree in storage.
     */
    public function update(Request $request, Degree $degree): \Illuminate\Http\RedirectResponse
    {
        $validatedData = $request->validate([
            'user_id' => 'required|exists:users,id',
            'university_id' => 'required|exists:universities,id',
            'title' => 'required|string|max:255',
            'major' => 'nullable|string|max:255',
            'graduation_date' => 'required|date',
            'status' => 'required|string|in:issued,verified,revoked', // Allow status update
        ]);

        $degree->update($validatedData);

        return redirect()->route('degrees.index')->with('success', 'Degree updated successfully!');
    }

    /**
     * Remove the specified degree from storage.
     */
    public function destroy(Degree $degree): \Illuminate\Http\RedirectResponse
    {
        $degree->delete();
        return redirect()->route('degrees.index')->with('success', 'Degree deleted successfully!');
    }

    /**
     * Download the certificate for the specified degree.
     * In a real application, this would fetch the actual certificate file,
     * potentially from IPFS or a secure storage linked by the blockchain hash.
     */
    public function downloadCertificate(Degree $degree): \Symfony\Component\HttpFoundation\StreamedResponse
    {
        // For demonstration, let's assume certificates are stored in storage/app/certificates
        // and named after their degree_hash.pdf
        $filePath = 'certificates/' . $degree->degree_hash . '.pdf';

        if (Storage::exists($filePath)) {
            return Storage::download($filePath, 'certificate_' . $degree->user->name . '_' . $degree->title . '.pdf');
        } else {
            // Handle case where certificate file does not exist
            abort(404, 'Certificate file not found.');
        }
    }
