    {
        return view('degrees.show', compact('degree'));
    }

    /**
     * Show the form for editing the specified degree.
     */
    public function edit(Degree $degree): \Illuminate\Contracts\View\View|\Illuminate\Foundation\Application|\Illuminate\Contracts\View\Factory|\Illuminate\Contracts\Foundation\Application
    {
        $students = User::where('role', 'student')->get();
        $universities = University::all();
